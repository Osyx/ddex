export const SUPPORTED_LANGUAGES = Object.keys(LANGUAGE_MAP).toSorted();

export const buildStopWordSet = (langs: string[]): Set<string> => {
  const words = new Set<string>();
  for (const lang of langs) {
    const list = LANGUAGE_MAP[lang];
    if (!list) {
      throw new Error(
        `Unsupported language code: "${lang}". Supported codes: ${SUPPORTED_LANGUAGES.join(", ")}`,
      );
    }
    for (const w of list) words.add(w.toLowerCase());
  }
  return words;
};

export const isStopWord = (word: string, stopWords: Set<string>): boolean => {
  return stopWords.has(word.toLowerCase());
};
